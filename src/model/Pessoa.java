package model;

/**
 *
 * @author Avell
 */
public class Pessoa {

    private String id;
    private String nome;

    public Pessoa(int id, String nome) {
        this.id = null;
        this.nome = nome;
    }

    public Pessoa() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

}
