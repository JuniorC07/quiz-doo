    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DAOLogin dl = new DAOLogin();
        User user = new User();
        try {
            user = dl.login(this.txtlogin.getText(), String.valueOf(this.txtSenha.getPassword()));
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (user.isExiste()) {
            if (user.isProfessor()) {
                TelaProfessor p = new TelaProfessor();
                p.mainProfessor(user.getP());
                this.dispose();
            } else {
                TelaAluno tl = new TelaAluno();
                tl.mainAluno(user.getA());
                this.dispose();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Login ou senha inv√°lidos, Tente Novamente");
            this.txtlogin.setText("");
            this.txtSenha.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

